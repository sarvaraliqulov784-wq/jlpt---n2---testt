// RESULT
// ================================================================
function showResult() {
  saveResult(currentTest.id, correctCount, currentQuestions.length);
  const pct = Math.round((correctCount / currentQuestions.length) * 100);
  const pass = pct >= 60;

  document.getElementById('result-pct').textContent = pct + '%';
  const circle = document.getElementById('result-circle');
  circle.className = 'result-circle ' + (pass ? 'pass' : 'fail');
  document.getElementById('result-title').textContent = pass ? '合格ライン達成！🎉' : 'もう一度チャレンジ！';
  document.getElementById('result-msg').textContent = pass ? 'すばらしい結果です。次のテストへ進みましょう。' : '復習してもう一度挑戦しましょう。';
  document.getElementById('r-correct').textContent = correctCount;
  document.getElementById('r-wrong').textContent = currentQuestions.length - correctCount;
  document.getElementById('r-total').textContent = currentQuestions.length;

  showScreen('result-screen');
  renderMavzuList();
  updateStats();
}

function retryTest() {
  startTest(currentTest.id);
}

// ================================================================
// NAVIGATION
// ================================================================
function goHome() {
  showScreen('home-screen');
}

function showScreen(id) {
  document.querySelectorAll('.screen').forEach(s => s.classList.remove('active'));
  document.getElementById(id).classList.add('active');
  window.scrollTo(0, 0);
}


