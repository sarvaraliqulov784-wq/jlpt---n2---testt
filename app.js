function startTest(testId) {
  const test = ALL_TESTS.find(t => t.id === testId);
  if (!test) return;
  currentTest = test;
  currentQuestions = test.sections.flatMap(s =>
    s.qs.map(q => ({...q, secTitle: s.title, secInst: s.inst, mondai: s.mondai}))
  );
  currentIndex = 0;
  correctCount = 0;
  answered = false;
  document.getElementById('test-title').textContent = test.name;
  showScreen('test-screen');
  renderQuestion();
}

function renderQuestion() {
  const q = currentQuestions[currentIndex];
  answered = false;

  // Progress
  const pct = Math.round((currentIndex / currentQuestions.length) * 100);
  document.getElementById('progress-fill').style.width = pct + '%';
  document.getElementById('progress-text').textContent = (currentIndex + 1) + ' / ' + currentQuestions.length;

  // Mondai label
  document.getElementById('mondai-label').textContent = '問題' + q.mondai + ' — ' + q.secTitle;

  // Question text
  document.getElementById('question-text').innerHTML = q.q.replace(/\n/g, '<br>');

  // Options
  const optsDiv = document.getElementById('options');
  optsDiv.innerHTML = '';
  q.opts.forEach((opt, i) => {
    const btn = document.createElement('button');
    btn.className = 'option-btn';
    btn.innerHTML = `<span class="option-num">${i + 1}</span>${opt}`;
    btn.onclick = () => selectAnswer(i);
    optsDiv.appendChild(btn);
  });

  // Hide feedback and next button
  const fb = document.getElementById('feedback');
  fb.className = 'feedback';
  fb.innerHTML = '';
  document.getElementById('next-btn').style.display = 'none';
}

function selectAnswer(idx) {
  if (answered) return;
  answered = true;

  const q = currentQuestions[currentIndex];
  const btns = document.querySelectorAll('.option-btn');

  btns.forEach((btn, i) => {
    btn.disabled = true;
    if (i === q.ans) btn.classList.add('correct');
    else if (i === idx) btn.classList.add('wrong');
  });

  const fb = document.getElementById('feedback');
  if (idx === q.ans) {
    correctCount++;
    fb.className = 'feedback show correct-fb';
    fb.innerHTML = '✓ 正解！' + (q.note ? '<br><small>' + q.note + '</small>' : '');
  } else {
    fb.className = 'feedback show wrong-fb';
    fb.innerHTML = '✗ 不正解。正解は <strong>' + (q.ans + 1) + '</strong> ' + q.opts[q.ans] + (q.note ? '<br><small>' + q.note + '</small>' : '');
  }

  document.getElementById('next-btn').style.display = 'block';
  document.getElementById('next-btn').textContent =
    currentIndex + 1 >= currentQuestions.length ? '結果を見る →' : '次へ →';
}

function nextQuestion() {
  currentIndex++;
  if (currentIndex >= currentQuestions.length) {
    showResult();
  } else {
    renderQuestion();
  }
}

